package aws.dynamodb.util;

import java.util.Arrays;

import com.amazonaws.services.dynamodbv2.document.DynamoDB;
import com.amazonaws.services.dynamodbv2.document.Table;
import com.amazonaws.services.dynamodbv2.model.AttributeDefinition;
import com.amazonaws.services.dynamodbv2.model.KeySchemaElement;
import com.amazonaws.services.dynamodbv2.model.KeyType;
import com.amazonaws.services.dynamodbv2.model.ProvisionedThroughput;
import com.amazonaws.services.dynamodbv2.model.ScalarAttributeType;

import aws.dynamodb.connection.DynamoDBConnection;

public class DynamoDBTable {

    public static void main(String[] args) throws Exception {

        String tableName = "Records";

        try {
            System.out.println("Attempting to create table; please wait...");
            DynamoDB dynamoDB = DynamoDBConnection.createConnection();
            Table table = dynamoDB.createTable(tableName,
                Arrays.asList(new KeySchemaElement("id", KeyType.HASH)),
                Arrays.asList(new AttributeDefinition("id", ScalarAttributeType.N)),
                new ProvisionedThroughput(10L, 10L));
            table.waitForActive();
            System.out.println("Success.  Table status: " + table.getDescription().getTableStatus());

        }
        catch (Exception e) {
            System.err.println("Unable to create table: ");
            System.err.println(e.getMessage());
        }

    }
}